        <div class="content">物理防治是利用机械设备或物理因素来诱捕杀灭蝗虫的方法。其通过利用蝗虫的趋化性（特殊食物）等特性设计诱捕器，或者利用激光辐射改变蝗虫活动性，使蝗虫致畸或绝育的方法实现防治的目的。
            物理防治方法有无污染，操作安全等优点[2]。</div>
