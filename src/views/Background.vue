        <div class="headline">我们的方法——利用蝗虫群聚信息素</div>
        <div class="content">国际著名昆虫学家和蝗虫学之父尤瓦洛夫发现飞蝗之所以能成灾是因为蝗虫可以从低密度的散居型转变为高密度的群居型[4]。上世纪七十年代，国际上科学家们才逐步认识到群聚信息素可能是蝗虫能够聚集的最关键因素[5]。经过50多年几代科学家的不断努力，有几种化合物被是蝗虫的群聚信息素，其中4-乙烯基苯甲醚（4VA，4-vinylanisole）、愈创木酚（guaiacol）被发现能够剂量依赖地聚集蝗虫[6]。因此，可以利用上述群聚信息素分析蝗虫种群密度，做到蝗灾的早期预防。同时也可利用群聚信息素吸引聚集蝗虫，提高灭杀的效率。
